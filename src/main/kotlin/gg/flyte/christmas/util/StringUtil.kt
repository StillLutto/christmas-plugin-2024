package gg.flyte.christmas.util

fun String.toSmallText(): String = lowercase()
    .replace("q", "ꞯ")
    .replace("w", "ᴡ")
    .replace("e", "ᴇ")
    .replace("r", "ʀ")
    .replace("t", "ᴛ")
    .replace("y", "ʏ")
    .replace("u", "ᴜ")
    .replace("i", "ɪ")
    .replace("o", "ᴏ")
    .replace("p", "ᴘ")
    .replace("a", "ᴀ")
    .replace("s", "ѕ")
    .replace("d", "ᴅ")
    .replace("f", "ꜰ")
    .replace("g", "ɢ")
    .replace("h", "ʜ")
    .replace("j", "ᴊ")
    .replace("k", "ᴋ")
    .replace("l", "ʟ")
    .replace("z", "ᴢ")
    .replace("x", "x")
    .replace("c", "ᴄ")
    .replace("v", "ᴠ")
    .replace("b", "ʙ")
    .replace("n", "ɴ")
    .replace("m", "ᴍ")
